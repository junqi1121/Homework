#define _CRT_SECURE_NO_WARNINGS
#include<stdio.h>
#include<math.h>
#define GESHU 100//所求结果个数
int multiply(int x, int y)
{
	int n = 1;
	for (int i = 1; i <= y; i++)
	{
		n *= x;
	}
	return n;
}
int isprime(int x)
{
	int count = 1;
	for (int i = 2; i <= sqrt(x); i++)
	{
		if (x % i == 0)
		{
			count = 0;
			break;
		}
	}
	return count;
}
int main()
{
	int p = 2, q = 2;
	int count = 0;
	while (1)
	{
		if (isprime(p) == 1 && isprime(q) == 1)
		{
			if (isprime(multiply(p, q) + multiply(q, p)) == 1)
			{
				printf("%d %d\n", p, q);
				count++;
			}
		}
		if (count == GESHU)//确定求得结果的个数，在宏定义处修改
		{
			break;
		}
		q++;
	}
	return 0;
}
